package eu.trentorise.smartcampus.ac.provider.managers;

public class SocialEngineException extends Exception {

	private static final long serialVersionUID = 3608702016888822580L;

	public SocialEngineException() {
		super();
	}

	public SocialEngineException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public SocialEngineException(String arg0) {
		super(arg0);
	}

	public SocialEngineException(Throwable arg0) {
		super(arg0);
	}

}
